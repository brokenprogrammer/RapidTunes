package me.oskarmendel.api.spotify;

/**
 * A Spotify Image object.
 * 
 * @author Oskar Mendel
 * @version 0.00.00
 * @name Image.java
 */
public class Image {
	
	private int width;
	private int height;
	private String url;
	
	/**
	 * Default constructor for the Image object creating an empty Image object. 
	 */
	public Image() {
		
	}
	
	/**
	 * Constructs a Image object with the values of the specified parameters.
	 * 
	 * @param width - The image width in pixels.
	 * @param height - The image height in pixels.
	 * @param url - The source URL of the image.
	 */
	public Image(int width, int height, String url) {
		this.width = width;
		this.height = height;
		this.url = url;
	}

	/**
	 * @return the width
	 */
	public int getWidth() {
		return width;
	}

	/**
	 * @param width the width to set
	 */
	public void setWidth(int width) {
		this.width = width;
	}

	/**
	 * @return the height
	 */
	public int getHeight() {
		return height;
	}

	/**
	 * @param height the height to set
	 */
	public void setHeight(int height) {
		this.height = height;
	}

	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
}
